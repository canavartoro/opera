var changeErpTur = function (value) {
    if (value == 0)
        return "UyumSoft (WebErp)";
    else if (value == 1)
        return "UyumSoft (Progress)";
    else if (value == 2)
        return "Logo";
    else if (value == 3)
        return "Sap";
    else if (value == 4)
        return "Netsis";
    else if (value == 5)
        return "Mikro";
    else if (value == 6)
        return "Axapta";
    else if (value == 7)
        return "Diger";
    //var template = '<span style="color:{0};">{1}</span>';
    //return Ext.String.format(template, (value > 0) ? "green" : "red", value);
};